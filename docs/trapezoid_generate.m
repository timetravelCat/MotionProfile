function trapezoid = trapezoid_generate(pi,pf,vi,vf,vmax,vmin,amax,amin)
trapezoid.pi = pi;
trapezoid.pf = pf;
trapezoid.vi = vi;
trapezoid.vf = vf;
trapezoid.vmax = vmax;
trapezoid.vmin = vmin;
trapezoid.amax = amax;
trapezoid.amin = amin; 

% Then Calculate ta, tb, tc
trapezoid.ta;
trapezoid.tb;
trapezoid.tc;





trapezoid.t = trapezoid.ta + trapezoid.tb + trapezoid.tc;
end